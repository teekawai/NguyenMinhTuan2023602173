package b5_OutOfRange;

public class Nhap {
	int Nhap;

	public Nhap(int nhap) {
		Nhap = nhap;
	}

	public int getNhap() {
		return Nhap;
	}

	public void setNhap(int nhap) {
		Nhap = nhap;
	}

	@Override
	public String toString() {
		return "Nhap{" + "Nhap=" + Nhap + '}';
	}

}
